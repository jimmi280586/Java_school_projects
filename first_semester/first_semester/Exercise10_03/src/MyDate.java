
public class MyDate
{
   public int month;
   public int day;
   public int year;
   
   public void setmonth(int month)
   {
     this.month = month; 
   }
     
   public void setday(int day)
   {
      this.day = day;
   }
   
   public void setyear(int year)
   {
     this.year = year; 
   }
   
  public int getmonth()
  {
     return this.month;
  }
  
  public int getday()
  {
    return this.day; 
  }
  
  public int getyear()
  {
     return this.year;
  }
  

    
    

}

